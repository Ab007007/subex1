package com.subex.javatraining.conditional;

public class ForEach {

	
	public static void main(String[] args) {
		
		String str[] = new  String[5];
		
		str[0] = "Subex1";
		str[1] = "Subex2";
		str[2] = "Subex3";
		str[3] = "Subex4";
		str[4] = "Subex5";
		
		for(String s:str)
		{
			System.out.println(s);
		}
	
	
	}
}
